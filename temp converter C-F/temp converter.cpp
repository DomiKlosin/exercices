#include <iostream>
#include <iomanip>

using namespace std;

float temp_CtoF()
{
    float tempC, new_tempF;
    cin>>tempC;
    new_tempF = 9/5*tempC+32;
    return new_tempF;
}

float temp_FtoC()
{
    float tempF, new_tempC;
    cin>>tempF;
    new_tempC = 5/9*(tempF+32);
    return new_tempC;
}

int main()
{
    cout<<"Temperature in C: ";
    float new_temp1 = temp_CtoF();
    cout<<"Temperature in F: "<<new_temp1<<endl;

    cout<<"Temperature in F: ";
    float new_temp2 = temp_FtoC();
    cout<<"Temperature in C: "<<new_temp2<<endl;

    return 0;
}
